#pragma once

// Rgb показывает состояние рабочего экрана.
// Остальные экраны держат индикацию выключенной.
class Rgb {
public:
  void init(); // Настраивает пины и выключает все цвета.
  void off(); // Нет активного рабочего состояния.
  void ready(); // Зелёный: рабочий экран готов к старту.
  void dosing(); // Синий: идёт дозирование.
};
