#pragma once

// Минимальная обёртка над физическим сервоприводом.
// Она хранит текущий и целевой угол, а write9() вызывает только при изменении.
class Servo {
public:
  void init(int initialAngle); // Начальный угол при входе в экран/режим.
  void loop(); // Применяет targetAngle к физическому серво, если он изменился.
  void setAngle(int angle); // Задаёт целевой угол с ограничением безопасного диапазона.
  int getTargetAngle() const; // Последний запрошенный угол.

private:
  int currentAngle = -1; // -1 заставляет первый loop() обязательно записать угол.
  int targetAngle = 90; // Команда, которую надо отправить в серво.
};
